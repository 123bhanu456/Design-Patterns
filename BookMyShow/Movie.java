public class Movie {
    int movie_id;
    String title;
    Genre genre;
    int duration;
    double rating;

    Movie(int movie_id,String title,Genre genre,int duration,double rating){
        this.duration=duration;
        this.movie_id=movie_id;
        this.genre=genre;
        this.title=title;
        this.rating=rating;
    }









}
